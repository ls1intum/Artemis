#!/usr/bin/env python3

from tests.TestCompile import TestCompile
from tests.TestConfigure import TestConfigure
from tests.TestCatch2 import TestCatch2
from testUtils.Tester import Tester
import os

def main() -> None:
    # Create a new instance of the tester:
    tester: Tester = Tester()

    buildDir = "./build"

    # First make the correct CMakeLists.txt
    # Therefore, get all written tests
    test_filenames = {}
    for root, dirs, filenames in os.walk("."):
        dirs[:] = [d for d in dirs if d not in ['assignment', 'build']]

        for f in filenames:
            if f.endswith(".cpp") or f.endswith(".c"):
                rel_path = os.path.relpath(os.path.join(root, f), ".")
                rel_path = rel_path.replace(os.sep, "/")

                rel_path_no_ext, _ = os.path.splitext(rel_path)
                unique_name = rel_path_no_ext.replace("/", "_").replace(".", "_")

                test_filenames[unique_name] = rel_path

    print(f"Found tests: {test_filenames}")

    # And adjust CMakeLists.txt accordingly
    cmake_file = os.path.join('.', "CMakeLists.txt")
    if not os.path.isfile(cmake_file):
        raise FileNotFoundError("CMakeLists.txt not found")

    with open(cmake_file, "r", encoding="utf-8") as f:
        lines = f.readlines()

        new_lines = []
        in_auto_block = False
        markers_found = False
        marker_start = "# AUTOGENERATED_TESTS_START"
        marker_end = "# AUTOGENERATED_TESTS_END"

        for line in lines:
            if line.strip() == marker_start:
                in_auto_block = True
                markers_found = True
                new_lines.append(line)

                for test_name, test_path_name in test_filenames.items():
                    new_lines.append(f"add_executable({test_name} {test_path_name})\n")
                    new_lines.append(f"target_link_libraries({test_name} assignment Catch2::Catch2WithMain)\n")
                    new_lines.append(f"add_test(NAME {test_name} COMMAND {test_name})\n\n")
                continue

            if line.strip() == marker_end:
                in_auto_block = False
                new_lines.append(line)
                continue

            if not in_auto_block:
                new_lines.append(line)

        if not markers_found:
            new_lines.append(f"\n{marker_start}\n")
            for test_name, test_path_name in test_filenames.items():
                new_lines.append(f"add_executable({test_name} {test_path_name})\n")
                new_lines.append(f"target_link_libraries({test_name} assignment Catch2::Catch2WithMain)\n")
                new_lines.append(f"add_test(NAME {test_name} COMMAND {test_name})\n\n")
            new_lines.append(f"{marker_end}\n")

        with open(cmake_file, "w", encoding="utf-8") as f:
            f.writelines(new_lines)

    # Register all test cases:
    # Configure:
    testConfigure: TestConfigure = TestConfigure(".", buildDir,
                                                 ["-DCMAKE_BUILD_TYPE=Debug",
                                                  "-DCMAKE_CXX_FLAGS=-fsanitize=address",
                                                  "-DCMAKE_EXE_LINKER_FLAGS=-fsanitize=address"])
    tester.addTest(testConfigure)

    for test_name, _ in test_filenames.items():
        tester.addTest(TestCompile(buildDir, test_name, requirements=[testConfigure.name], name=f"Compile_{test_name}"))
        tester.addTest(TestCatch2(buildDir, test_name, ["Compile_" + test_name]))

    # Run the actual tests:
    tester.run()
    # Export the results into the JUnit XML format:
    tester.exportResult("./test-reports/tests-results.xml")


if __name__ == "__main__":
    main()
